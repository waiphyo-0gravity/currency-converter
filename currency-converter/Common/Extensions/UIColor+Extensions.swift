import UIKit

extension UIColor {
    @nonobjc class var primary: UIColor? {
        return UIColor(named: "primary")
    }
    
    @nonobjc class var secondary: UIColor? {
        return UIColor(named: "secondary")
    }
    
    @nonobjc class var c50: UIColor? {
        return UIColor(named: "c50")
    }
    
    @nonobjc class var c300: UIColor? {
        return UIColor(named: "c300")
    }
    
    @nonobjc class var s10: UIColor? {
        return UIColor(named: "s10")
    }
}

